
public class Badgers {
	public static void main(String[] args) {
		for (int i = 1; i < 4; i++) {
			System.out.println();
			for (int j = 1; j < 11; j++) {
				System.out.print("badger,");
			}
			System.out.println();
			for (int k = 1; k < 3; k++) {
				System.out.print("mushroom, ");
			}
		}
	}
}
